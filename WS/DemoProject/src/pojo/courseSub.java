package pojo;

import java.util.List;

public class courseSub {
	
	private List<webautomationSub> webAutomation;
	private List<apiSub> api;
	private List<mobileSub> mobile;
	public List<webautomationSub> getWebAutomation() {
		return webAutomation;
	}
	public void setWebAutomation(List<webautomationSub> webAutomation) {
		this.webAutomation = webAutomation;
	}
	public List<apiSub> getApi() {
		return api;
	}
	public void setApi(List<apiSub> api) {
		this.api = api;
	}
	public List<mobileSub> getMobile() {
		return mobile;
	}
	public void setMobile(List<mobileSub> mobile) {
		this.mobile = mobile;
	}
	
	

}
